import numpy as np
import random
import matplotlib.pyplot as plt
x = np.random.random([100])*10
e = np.random.randn(100)*2
m = 5
c=-2
y = m*x + c+ e
plt.scatter(x,y)
mcap = np.random.random(1)*10
ccap = np.random.random(1)*10

n = 100
lamda = 0.01
for i in range(1,n):
    dcostdmcap = -2/n * np.sum((y-(mcap*x + ccap))*x)
    mcap = mcap - lamda*dcostdmcap
    dcostdccap = -2/n * np.sum((y-(mcap*x+ccap)))
    ccap = ccap - lamda*dcostdccap
ycap = mcap*x + ccap
plt.plot(x,ycap)
plt.scatter(x,y)